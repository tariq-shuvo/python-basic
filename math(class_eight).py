# Python has a set of built-in math functions, including an extensive math module, that allows you to perform mathematical tasks on numbers.
import math

x = min(5, 10, 25)
y = max(5, 10, 25)

abs = abs(-7.56)

p1 = math.ceil(1.4)
p2 = math.floor(1.4)

print(x)
print(y)
print(abs)
print(p1)
print(p2)
print(pow(4, 3))
print(math.pi)
print(math.sqrt(64))